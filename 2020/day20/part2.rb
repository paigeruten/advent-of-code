def opposite(dir)
  { :n => :s, :s => :n, :e => :w, :w => :e }[dir]
end

class Tile
  attr_accessor :id, :rows, :position

  def initialize(id, rows)
    @id, @rows = id, rows
    @position = nil
  end

  def self.parse(str)
    lines = str.lines
    if lines[0] =~ /^Tile (\d+):$/
      tile_id = $1.to_i
    else
      p "oops"
      exit
    end

    rows = lines[1..-1].map(&:chomp)

    Tile.new(tile_id, rows)
  end

  def self.from_tile_map(tile_map)
    tile_size = tile_map[0][0].rows.length
    rows = []
    tile_map.map.each do |map_row|
      (0...tile_size).each do |y|
        rows << map_row.map { |tile| tile.rows[y] }.join
      end
    end

    Tile.new(0, rows)
  end

  def try_attaching!(other)
    edges.each do |dir, edge|
      other.edges.each do |other_dir, other_edge|
        if edge == other_edge
          case [dir, other_dir]
          when [:n, :n], [:s, :s]
            other.flip_vertically!
          when [:e, :e], [:w, :w]
            other.flip_horizontally!
          when [:n, :e], [:s, :w]
            other.flip_horizontally!
            other.rotate_ccw!
          when [:e, :s], [:w, :n]
            other.rotate_cw!
          when [:n, :w], [:s, :e]
            other.rotate_ccw!
          when [:e, :n], [:w, :s]
            other.flip_vertically!
            other.rotate_cw!
          end
        elsif edge == other_edge.reverse
          case [dir, other_dir]
          when [:n, :n], [:e, :e], [:s, :s], [:w, :w]
            other.rotate_cw!(2)
          when [:n, :e], [:s, :w]
            other.rotate_cw!
          when [:e, :s], [:n, :w], [:w, :n], [:s, :e]
            other.flip_horizontally!
            other.rotate_cw!
          when [:n, :s], [:s, :n]
            other.flip_horizontally!
          when [:e, :w], [:w, :e]
            other.flip_vertically!
          when [:e, :n], [:w, :s]
            other.rotate_ccw!
          end
        else
          next
        end
        return dir
      end
    end
    nil
  end

  def flip_horizontally!
    @rows.each(&:reverse!)
  end

  def flip_vertically!
    @rows.reverse!
  end

  def rotate_cw!(n = 1)
    n.times do
      @rows = (0...@rows.length).map do |i|
        @rows.map { |row| row[i] }.join
      end
      flip_horizontally!
    end
  end

  def rotate_ccw!(n = 1)
    n.times do
      @rows = (0...@rows.length).map do |i|
        @rows.map { |row| row[i] }.join
      end
      flip_vertically!
    end
  end

  def remove_border!
    @rows = @rows[1..-2]
    @rows.map! { |row| row[1..-2] }
  end

  def edges
    {
      :n => @rows.first,
      :s => @rows.last,
      :w => @rows.map { |row| row[0] }.join,
      :e => @rows.map { |row| row[-1] }.join
    }
  end

  def width
    @rows.first.length
  end

  def height
    @rows.length
  end

  def count_occurrences(other)
    num_occurrences = 0
    (0..(width - other.width)).each do |x|
      (0..(height - other.height)).each do |y|
        nope = false
        (0...other.width).each do |ox|
          (0...other.height).each do |oy|
            char = other.rows[oy][ox]
            if char != ' ' && @rows[y + oy][x + ox] != char
              nope = true
              break
            end
          end
          break if nope
        end
        num_occurrences += 1 unless nope
      end
    end
    num_occurrences
  end

  def count_chars(char)
    @rows.join.count(char)
  end
end

tiles = {}
DATA.read.split("\n\n").map do |tile_str|
  tile = Tile.parse(tile_str)
  tiles[tile.id] = tile
end

free_tiles = tiles.keys
attached_tiles = []
completed_tiles = []

starting_tile = free_tiles.shift
attached_tiles << starting_tile
tiles[starting_tile].position = [0, 0]

until free_tiles.empty?
  newly_attached_tiles = []
  attached_tiles.each do |tile_id|
    tile = tiles[tile_id]
    newly_attached_tiles += free_tiles.select do |free_tile|
      dir = tile.try_attaching!(tiles[free_tile])
      if dir
        x, y = tile.position
        x += { :n => 0, :e => 1, :s => 0, :w => -1 }[dir]
        y += { :n => -1, :e => 0, :s => 1, :w => 0 }[dir]
        if tiles.values.map(&:position).include?([x, y])
          p "uh oh"
          exit
        end
        tiles[free_tile].position = [x, y]
        true
      else
        false
      end
    end
    free_tiles -= newly_attached_tiles
  end
  completed_tiles += attached_tiles
  attached_tiles = newly_attached_tiles
end

xs = tiles.map { |_, tile| tile.position[0] }
ys = tiles.map { |_, tile| tile.position[1] }

