require 'set'

$map = []
$locs = []
$num_locs = 0
DATA.each.with_index do |line, y|
  $map << line.chomp
  line.to_enum(:scan, /\d/).map { Regexp.last_match.offset(0)[0] }.each do |x|
    $num_locs += 1
    $locs[line[x].to_i] = [x, y]
  end
end

$memo = {}
def shortest_dist(x1, y1, x2, y2)
  if $memo[[x1, y1, x2, y2]].nil?
    front = [[x1, y1]]
    been = Set.new(front)
    steps = 0
    found = false
    while !found
      new_front = []
      front.each do |x, y|
        [[0, -1], [1, 0], [0, 1], [-1, 0]].each do |dx, dy|
          newx, newy = dx + x, dy + y
          if newx == x2 and newy == y2
            $memo[[x1, y1, x2, y2]] = steps + 1
            $memo[[x2, y2, x1, y1]] = steps + 1
            found = true
            break
          elsif newx >= 0 and newy >= 0 and not [nil, ?#].include?($map[newy][newx]) and not been.member?([newx, newy])
            new_front << [newx, newy]
            been.add [newx, newy]
          end
        end
        break if found
      end
      front = new_front
      steps += 1
    end
  end

  $memo[[x1, y1, x2, y2]]
end

min_dist = nil
min_path = nil
(1...$locs.length).to_a.permutation.each.with_index do |path, i|
  cur = 0
  dist = 0
  (path + [0]).each do |loc|
    x1, y1 = $locs[cur]
    x2, y2 = $locs[loc]
    dist += shortest_dist(x1, y1, x2, y2)
    cur = loc
  end
  if min_dist.nil? or dist < min_dist
    min_dist = dist
    min_path = path
  end
end

p min_path
p min_dist

